package it.unibo.arces.wot.sepa.pattern;

import it.unibo.arces.wot.sepa.api.protocols.websocket.WebsocketSubscriptionProtocol;
import it.unibo.arces.wot.sepa.commons.exceptions.SEPAProtocolException;
import it.unibo.arces.wot.sepa.commons.exceptions.SEPASecurityException;
import it.unibo.arces.wot.sepa.commons.security.SEPASecurityManager;

public abstract class Consumer extends AbstractConsumer {

	public Consumer(JSAP appProfile, String subscribeID, SEPASecurityManager sm) throws SEPAProtocolException, SEPASecurityException {
		super(appProfile,subscribeID, sm, 
				new WebsocketSubscriptionProtocol(appProfile.getSubscribeHost(subscribeID), appProfile.getSubscribePort(subscribeID), appProfile.getSubscribePath(subscribeID)));
	}

}
